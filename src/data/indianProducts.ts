// Generate offers for each product
export const generateIndianOffers = (): Record<string, SellerOffer[]> => {
  const offers: Record<string, SellerOffer[]> = {};

  indianProducts.forEach((product) => {
    const productOffers: SellerOffer[] = [];

    // Determine which sellers would sell this product
    let relevantSellers: string[] = [];

    if (product.category === "electronics") {
      relevantSellers = ["flipkart", "amazon-in", "croma"];
    } else if (product.category === "fashion") {
      relevantSellers = ["flipkart", "amazon-in", "myntra"];
    } else if (product.category === "beauty") {
      relevantSellers = ["amazon-in", "myntra", "nykaa"];
    } else {
      relevantSellers = ["flipkart", "amazon-in"];
    }

    relevantSellers.forEach((sellerId, index) => {
      const basePrice = product.basePrice;
      const priceVariation = 1 + (Math.random() - 0.5) * 0.1; // ±5% price variation
      const offerPrice = Math.round(basePrice * priceVariation);
      const originalPrice = Math.round(offerPrice * (1 + Math.random() * 0.2)); // 0-20% discount

      productOffers.push({
        id: `offer_${product.id}_${sellerId}`,
        sellerId,
        productId: product.id,
        price: offerPrice,
        originalPrice,
        discount: Math.round(
          ((originalPrice - offerPrice) / originalPrice) * 100
        ),
        condition: "new",
        stockQuantity: Math.floor(Math.random() * 50) + 10,
        shippingOptions: [
          {
            id: "standard",
            name: "Standard Shipping",
            price: index === 0 ? 0 : Math.floor(Math.random() * 100),
            estimatedDays: 3 + Math.floor(Math.random() * 5),
            carrier: ["Blue Dart", "DTDC", "India Post", "Delhivery"][
              Math.floor(Math.random() * 4)
            ],
            tracking: true,
            insurance: Math.random() > 0.5,
          },
        ],
        returnPolicy: {
          returnsAccepted: true,
          returnWindow: [7, 15, 30][Math.floor(Math.random() * 3)],
          returnShipping: Math.random() > 0.5 ? "seller-pays" : "buyer-pays",
          conditions: [
            "Item must be in original condition",
            "Original packaging required",
          ],
        },
        authenticity: {
          guaranteed: true,
          verificationMethod: "Seller verification",
          certificate: "VERIFIED",
        },
        lastUpdated: new Date().toISOString(),
      });
    });

    // Sort offers by price (lowest first)
    offers[product.id] = productOffers.sort((a, b) => a.price - b.price);
  });

  return offers;
};
